# lidando com dicionários em Python que são por si só tabelas hash

# Definindo um dicionário
di = {'nome': 'Davi', 'idade': 23, 'profissao': 'programador'}

# Acessando os valores do dicionário, através de suas chaves
print(di['nome']) # -> 'Davi'
print(di['idade']) # -> 23
print(di['profissao']) # -> programador
